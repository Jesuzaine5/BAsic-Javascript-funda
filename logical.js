let age = 18;
let experience = 3;
let hasDegree = true;

console.log(age >= 18 && experience >1); //this is an example of AND operator 

console.log(age >= 18 || experience >1); // //this is an example of OR operator 

console.log(!hasDegree); //this is an example of NOT operator// this will invert the result instead of true, it will be false